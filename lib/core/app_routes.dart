      case teacher_grade_question:
        return MaterialPageRoute(
          builder: (context) {
            return T_ChooseGradeForQuestion(
              teachername: '',
              teachersubject: '',
              teacherid: '',
            );
          },
        );
      case teacher_class_question:
        return MaterialPageRoute(
          builder: (context) {
            return const T_ChooseClassForQuestion(
              teachername: '',
              teachersubject: '',
              teacherid: '',
              doc_id: '',
              studentgrade: '',
              studentclass: '',
            );
          },
        );

      default:
        return MaterialPageRoute(
          builder: (context) {
            return Scaffold(
              body: Center(
                child: Text("No route defined for ${setting.name}"),
              ),
            );
          },
        );
    }
  }
